#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	box;

	i = 0;
	j = 1;
	box = 0;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if ((str[i] == '+'
			|| str[i] == '-') && (str[i + 1] >= '0' && str[i] <= '9'))
	{
		if (str[i] == '-')
			j *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		box = (box * 10) + (str[i] - '0');
		i++;
	}
	return (box * j);
}
